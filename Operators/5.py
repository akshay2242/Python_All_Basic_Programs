# Program to find Smaller and Larger number

a = int(input("Enter the value of a: "))
b = int(input("Enter the value of b: "))

if a>b:
    print("a is Larger number and b is Smaller number")

elif a<b:
    print("a is Smaller number and b is Larger number")

else:
    print("The values of a and b are same")